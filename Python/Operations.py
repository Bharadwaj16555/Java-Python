# Operations.Arithmetic.py

a=3
b=3
sum = a + b
diff=a - b
mult=a * b
div=a / b
mod = a % b
print("The sum of", a, "and", b, "is", sum)
print("The difference between", a, "and", b, "is", diff)
print("The product of", a, "and", b, "is", mult)
print("The division of", a, "by", b, "is", div)
print("The modulus of", a, "and", b, "is", mod)
print("This is a simple arithmetic operations program.")


# Operations.Comparison.py
a = 5
b = 10
print("Is", a, "equal to", b, "?", a == b)
print("Is", a, "not equal to", b, "?", a != b)
print("Is", a, "greater than", b, "?", a > b)
print("Is", a, "less than", b, "?", a < b)
print("Is", a, "greater than or equal to", b, "?", a >= b)
print("Is", a, "less than or equal to", b, "?", a <= b)


# Operations.Assignment.py
a = 5
b = 10
a += b  # a = a + b
b -= a  # b = b - a
c = a * b  # c = a * b
print("After assignment operations:")
print("a =", a)
print("b =", b)
print("c =", c)


# Operations.Logical.py
a = True
b = False
print("a and b:", a and b)
print("a or b:", a or b)
print("not a:", not a)